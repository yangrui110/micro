var apiClient=(function(){
    var apiUrl="http://localhost:8282/microblog";
     function onSuccess(data,callback){
         if(data.code==200){
             callback(data.data);
         }else{
             alert(data.msg);
         }
     }
     return {
        deleteBannerById:function(id,callback){
    		HttpUtils.doDelete({
    			url:apiUrl+"/banner/deleteById?id="+id,
    			success:function(data){
    				onSuccess(data,callback);
    			}
    		});
    	},
        updateBanner:function(data,callback){
        	HttpUtils.doPost({
        		url:apiUrl+"/banner/save",
        		data:data,
        		success:function(data){
        			onSuccess(data,callback);
        		}
        	});
        },

        deleteBannerSelect:function(data,callback){
        	HttpUtils.doDelete({
        		url:apiUrl+"/banner/deleteSelect",
        		data:data,
        		success:function(data){
        			onSuccess(data,callback);
        		}
        	});
        },
        login:function(data,callback){
        	HttpUtils.doPost({
        		url:apiUrl+"/login",
        		data:data,
        		contentType:'application/x-www-form-urlencoded',
        		success:function(data){
        			onSuccess(data,callback);
        		}
        	});
        },
        deleteArticleById:function(id,callback){
        	HttpUtils.doDelete({
        		url:apiUrl+"/article/deleteById",
        		data:{
        			id:id
        		},
        		success:function(data){
        			onSuccess(data,callback);
        		}
        	});
        },
        updateArticle:function(data,callback){
        	HttpUtils.doPost({
        		url:apiUrl+"/article/save",
        		data:data,
        		success:function(data){
        			onSuccess(data,callback);
        		}
        	});
        },
        deleteArticleSelect:function(data,callback){
        	HttpUtils.doDelete({
        		url:apiUrl+"/artic/deleteSelect",
        		data:data,
        		success:function(data){
        			onSuccess(data,callback);
        		}
        	});
        },
    }
})(); 